/**
 * 正则表达式
 */
export const Regs = {
    // 身份证
    ID_CARD: /^(\d{6})(\d{4})(\d{2})(\d{2})(\d{3})([0-9]|X)$/,
    // 手机号
    PHONE: /^1[3-9]\d{9}$/,
    // 座机（传真）
    LANDLINE: /0\d{2,3}-\d{7,8}-\d{1,6}/,
    URL: /^(https?:\/\/(([a-zA-Z0-9]+-?)+[a-zA-Z0-9]+\.)+[a-zA-Z]+)(:\d+)?(\/.*)?(\?.*)?(#.*)?$/,
}
